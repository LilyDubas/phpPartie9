      <h1>D'où viennent les saisons?</h1>
      <p>
        Les saisons sont dues à la rotation de la Terre autour du Soleil, ainsi qu’à l’inclinaison de notre planète.
        En effet, la Terre pointe toujours dans la même direction et tourne autour d’un axe allant du pôle Nord au pôle Sud.
      </p>
      <img src="assets/img/saison-inclinaison.jpg" alt="inclinaison de la Terre">
      <p>
        Les faces les plus exposées au soleil changent en fonction de l’avancement de la Terre dans la rotation.
      </p>
      <p>
        Ainsi, du mois de juin au mois d'août, c’est l’été dans l’hémisphère nord, qui est à ce moment là plus proche et exposé plus longtemps au soleil.
        Logiquement, au même moment c'est l’hiver dans l’hémisphère sud, qui bénéficie de moins de soleil.
      </p>
      <p>
        De décembre à février, c’est l’inverse, l’hémisphère sud est en été tandis que l’hémisphère nord est en hiver.
      </p>
      <p>
        <span class="text-secondary">Au niveau de l’équateur,</span> les saisons sont beaucoup moins marquées. Ainsi, on peut considérer qu’il n’y en a qu’une. Seul l’ensoleillement peut varier, en fonction des solstices et des équinoxes. En résumé, il n’y a pas vraiment de différence entre les météos de décembre, d’avril ou d’août.
      </p>
      <p>
        <span class="text-secondary">Au niveau des pôles,</span> en revanche, les saisons sont très marquées. Comme la Terre tourne autour de l’axe nord-sud sur un pan incliné, les pôles ne sont pas du tout éclairés de la même façon.
      </p>
      <img src="/assets/img/saison-rotation.gif" alt="inclinaison de la Terre gif">
